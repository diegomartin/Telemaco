package com.diegomartin.telemaco.model;

public class Language {
	private String name;
	private String code;
	
	public Language(){ }

	public void setName(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getCode() {
		return code;
	}
	
}
